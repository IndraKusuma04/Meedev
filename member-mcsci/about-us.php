			<h2 class="mt-40">Perjalanan MCSCI</h2>
			<p style="text-align: justify;">Di awal tahun 2011, MCFC Indonesia mengadakan acara gathering dan Rakernas pertamanya yang di adakan di kota Bandung tepatnya di daerah Lembang untuk membicarakan, struktur organisasi, serta merancang AD/ART. Di pertengahan tahun, kami menyambut kedatangan piala FA ke Jakarta selama tiga hari. Pada saat itu, kami terus mengikuti dan “mengawal” piala yang telah dijuarai oleh Manchester City pada Mei 2011 saat mengalahkan Stoke City.</p>
			<p style="text-align: justify;">Djoeragan Koffie Batavia adalah Home Base pertama kali MCFCI yang berada di Jl.Kapten Tendean, Mampang Jakarta Selatan, dari sana kegiatan Nobar pun intens kami lakukan setiap minggunya selama ada jadwal pertandingan Manchester City. MCFCI mulai melebarkan sayap, dengan berkomunikasi menggunakan jejaring sosial seperti Facebook dan Twitter, beberapa daerah seperti Aceh, Makassar, Manado dan beberapa daerah lain mulai membentuk Branch resmi MCFC Indonesia.</p>
		</div>
		<div class="col-md-6">
			<h2 class="mt-40">Pengukuhan Secara Resmi</h2>
			<p style="text-align: justify;">Pada akhir 2011 pengurus MCFC Indonesia, mencari koneksi untuk mengukuhkan MCFC Indonesia sebagai organisasi supporter resmi klub Manchester City di tanah air. Dengan di bantu seorang teman yang berada di Ibukota Inggris, memberikan kontak langsung melalui email kepada sekretaris jenderal organisasi pendukung resmi Manchester City yang bernama Kevin Parker. Lalu beliau memberitahukan tata cara menjadi cabang organisasi resmi dari MCFC SC. Setelah memenuhi persyaratan yang diberikan, MCFC Indonesia pun dikukuhkan menjadi cabang resmi organisasi MCFC Supporters Club di Indonesia. Pengukuhan secara resmi tersebut ditandai dengan pemberian Member Card resmi serta pencantuman branch MCFCSC di situs resmi mereka yaitu mcfcsc.com, atau secara detail bisa dilihat dalam link ini : http://www.mcfcsc.com/index.php/branches/id/indonesia.  Pengukuhan secara resmi tersebut sekaligus menjadi kado ulang tahun MCFC Indonesia yang pertama.</p>
		</div>
		<div class="col-md-6">
			<h2 class="mt-40">Perkembangan MCSCI</h2>
			<p style="text-align: justify;">Di awal 2012, sudah ada 15 branch resmi yang tersebar di seluruh Indonesia.  Ada beberapa kejadian unik yang terjadi, salah satunya adalah anggota MCFC Indonesia yang bernama Dian Permata Putri, videonya saat menyanyikan lagu “Blue Moon” dan “The Beat Goes On” mendapat perhatian dari situs resmi Manchester City, mcfc.co.uk dan video tersebut masuk ke dalam rubric “Fans” di dalam situs tersebut. Pada bulan Agustus, MCFC Indonesia berkesempatan menonton skuat asuhan Roberto Mancini tersebut bertanding di Malaysia. Puluhan anggota MCFC Indonesia pun melakukan perjalanan ke negeri jiran tersebut untuk melihat pertandingan Manchester City  vs  Timnas Malaysia. Selain menonton Manchester City bertanding, kami juga bertemu dengan beberapa perwakilan MCFC dari Negara-negara di Asia seperti Malaysia, Singapura, dan Thailand.</p>
		</div>
		<div class="col-md-6">
			<h2 class="mt-40">Perubahan Nama Dan Restrukturisasi</h2>
			<p style="text-align: justify;">Di penghujung tahun 2012, selain memperingati hari jadi kedua MCFC Indonesia, para pengurus juga mengadakan perubahan logo dan nama dari MCFCI menjadi ICSC (Indonesia Citizens Supporters Club) tapi menggunakan nama baru tersebut pun tidak bertahan lama dan kemudian di ambil kesepakatan baru untuk merubah nama menjadi MCSCI (Manchester City Supporters Club Indonesia ) dan kemudian memilih Saudara Jimmy sebagai Ketua Umum yang baru.</p>
